#定义列表变量
list = ["C" , "python", "C++" , "Java" , "Julia" ]
#显示列表中所有数据信息
print("\n我喜欢的编程语言是：",list)
#使用下标索引来访问列表中的值
print ("\n\n列表中的第一个值: ", list[0])
print ("列表中的第三个值: ", list[2])
#使用中括号的形式截取字符
print ("\n\n列表中的第二和第三个值: ", list[1:3])
#利用for循环语句来遍历列表中的值
print("\n\n利用for循环语句来遍历列表中的值:")
for i in list:
    print(i)

