x = 128             #整型变量x，并赋值为128
y = 15              #整型变量y，并赋值为15
print("变量x的初始值为:",x,"   变量y的初始值为：",y )
y += x
print("y += x后，变量y的值为：",y)
y -= x
print("y -= x后，变量y的值为：",y)
y *= x
print("y *= x后，变量y的值为：",y)
y /= x
print("y /= x后，变量y的值为：",y)
y %= x
print("y %= x后，变量y的值为：",y)
y //= x
print("y //= x后，变量y的值为：",y)
y **= x
print("y **= x后，变量y的值为：",y)
