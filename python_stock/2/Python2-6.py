num1 = input("请输入第一个数：")        #用于计算的第一个数
num2 = input("请输入第二个数：")        #用于计算的第二个数
print("第一个数是：%d， 第二个数是：%d " %(int(num1),int(num2)))
print()
print("两个数相加：",int(num1)+int(num2))
print("两个数相减：",int(num1)-int(num2))
print("两个数相乘：",int(num1)*int(num2))
print("两个数相除：",int(num1)/int(num2))
print("两个数相除，求余数，即取模：",int(num1)%int(num2))
print("两个数相除，但只取商的整数部分：",int(num1)//int(num2))
print("幂，即返回x的y次幂：",int(num1)**int(num2))
